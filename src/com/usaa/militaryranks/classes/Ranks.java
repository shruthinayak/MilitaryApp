package com.usaa.militaryranks.classes;

public class Ranks {
	private String payGrade;
	private String abbreviation;
	private String title;
	private String insigniaImage;
	private String insigniaImageHD;
	public String getPayGrade() {
		return payGrade;
	}
	public void setPayGrade(String payGrade) {
		this.payGrade = payGrade;
	}
	public String getAbbreviation() {
		return abbreviation;
	}
	public void setAbbreviation(String abbreviation) {
		this.abbreviation = abbreviation;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getInsigniaImage() {
		return insigniaImage;
	}
	public void setInsigniaImage(String insigniaImage) {
		this.insigniaImage = insigniaImage;
	}
	public String getInsigniaImageHD() {
		return insigniaImageHD;
	}
	public void setInsigniaImageHD(String insigniaImageHD) {
		this.insigniaImageHD = insigniaImageHD;
	}

}
